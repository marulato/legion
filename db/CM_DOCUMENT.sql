CREATE TABLE CM_DOCUMENT
(
    ID                BIGINT PRIMARY KEY AUTO_INCREMENT,
    FILE_NAME         VARCHAR(256) NOT NULL,
    DOC_TYPE          VARCHAR(32),
    DOC_REF_NO        VARCHAR(32),
    VERSION           INT,
    STATUS            VARCHAR(32)  NOT NULL,
    CATEGORY          VARCHAR(16),
    DESCRIPTION       VARCHAR(1000),
    FILE_NET_ID       BIGINT       NOT NULL,
    IS_UPLOADED       CHAR(1),
    UPLOADED_FOR      VARCHAR(256),
    UPLOADED_BY       VARCHAR(32),
    UPLOADED_AT       DATETIME,
    IS_AUTO_GENERATED CHAR(1),
    GENERATED_FOR     VARCHAR(256),
    GENERATED_BY      VARCHAR(32),
    GENERATED_AT      DATETIME,
    IS_DELETED        CHAR(1),
    DELETED_FOR       VARCHAR(256),
    DELETED_BY        VARCHAR(32),
    DELETED_AT        DATETIME,
    IS_VOID           CHAR(1),
    VOIDED_FOR        VARCHAR(256),
    VOIDED_BY         VARCHAR(32),
    VOIDED_AT         DATETIME,
    REFERENCE_TBL     VARCHAR(64),
    REFERENCE_ID      BIGINT,
    CREATED_AT        DATETIME     NOT NULL,
    CREATED_BY        VARCHAR(32)  NOT NULL,
    UPDATED_AT        DATETIME     NOT NULL,
    UPDATED_BY        VARCHAR(32)  NOT NULL
);

